	<h4><i class="icon-list-ul"></i> Follow these steps to get Robo One Page template perfectly working</h4>
	<ol>
		<li>Create a page and assign "Homepage" template from the Page Attributes section on the right side</li>
		<li>Go to Settings->Reading from admin panel, select "A Static Page" and set the page you just created from the "Front Page" dropdown  </li>
		<li>You are almost done. Now log into your admin panel and start creating Sliders, Features, Team Members and Posts and assign those posts to this "Home Page" you created in step 1, from the "Connected Pages" section in the right sidebar.</li>
		<li>Enjoy.</li>
	</ol>
